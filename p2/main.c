#include <stdio.h>

int main(void) {
    int N;
    int a[20], b[20];

    // 배열 이름 대신 포인터를 이용해 원소에 접근하라는 조건을 만족시키기 위해 선언
    int *pa;
    int *pb;

    scanf("%d", &N);

    // 첫 번째 배열의 시작 주소부터 N개의 값을 차례대로 저장하기 위해 사용
    // pa가 한 칸씩 이동하면서 배열의 각 원소 위치를 가리킴
    for (pa = a; pa < a + N; pa++) {
        scanf("%d", pa);
    }

    // 두 번째 배열도 같은 방식으로 저장
    // 배열 첨자[]를 사용하지 않고 포인터만으로 입력받기 위해 사용
    for (pb = b; pb < b + N; pb++) {
        scanf("%d", pb);
    }

    // 첫 번째 배열은 앞에서부터 읽어야 하므로 시작 주소를 다시 저장
    pa = a;

    // 두 번째 배열은 역방향으로 읽어야 하므로 마지막 원소 주소를 저장
    pb = b + N - 1;

    // 첫 번째 배열의 마지막 원소까지 계산하기 위해 반복
    while (pa < a + N) {

        // 첫 번째 배열은 앞에서부터,
        // 두 번째 배열은 뒤에서부터 접근하여 더한 값을 출력
        // 문제에서 공백을 먼저 출력하라고 했으므로 " %d" 형식 사용
        printf(" %d", *pa + *pb);

        // 첫 번째 배열은 다음 원소와 계산해야 하므로 한 칸 앞으로 이동
        pa++;

        // 두 번째 배열은 역방향 계산을 계속해야 하므로 한 칸 뒤로 이동
        pb--;
    }

    return 0;
}
